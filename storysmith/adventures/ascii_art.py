def get_art(name: str) -> str:
    """Retrieve ASCII art by name.

    Args:
        name: The identifier for the art piece (case-insensitive).
              Use list_art() to see all available names.

    Returns:
        The ASCII art string.

    Raises:
        KeyError: If the art name is not found in the registry.

    Example:
        >>> art = get_art("CITADEL_LORD")
        >>> print(art)
    """
    normalized = name.upper().replace("-", "_").replace(" ", "_")
    if normalized not in _ART_REGISTRY:
        available = ", ".join(sorted(_ART_REGISTRY.keys()))
        raise KeyError(
            f"Art '{name}' not found. Available: {available}"
        )
    return _ART_REGISTRY[normalized]


def list_art() -> List[str]:
    """List all available ASCII art piece names.

    Returns:
        A sorted list of all art identifiers that can be passed to get_art().

    Example:
        >>> for name in list_art():
        ...     print(name)
    """
    return sorted(_ART_REGISTRY.keys())


def get_lock_for_tier(tier: int) -> str:
    """Get the appropriate lock art for a given difficulty tier.

    Args:
        tier: Lock difficulty from 1-6.

    Returns:
        The ASCII art for the corresponding lock tier.

    Raises:
        ValueError: If tier is not between 1 and 6.

    Example:
        >>> lock = get_lock_for_tier(3)
        >>> print(lock)
    """
    if not 1 <= tier <= 6:
        raise ValueError(f"Tier must be 1-6, got {tier}")
    return get_art(f"LOCK_TIER_{tier}")


def get_chapter_banner(chapter_id: str) -> str:
    """Get the chapter banner for a given chapter.

    Args:
        chapter_id: The chapter identifier (e.g., "THE_OUTER_GATES").

    Returns:
        The ASCII art banner for the chapter.

    Raises:
        KeyError: If the chapter is not found.
    """
    valid_chapters = ["THE_OUTER_GATES", "THE_CRYPTS", "THE_INNER_SANCTUM"]
    normalized = chapter_id.upper().replace("-", "_").replace(" ", "_")

    if normalized not in valid_chapters:
        raise KeyError(
            f"Chapter '{chapter_id}' not found. "
            f"Available chapters: {', '.join(valid_chapters)}"
        )
    return get_art(normalized)


def print_art(name: str) -> None:
    """Print ASCII art to stdout.

    A convenience function that retrieves and prints art in one call.

    Args:
        name: The identifier for the art piece.

    Example:
        >>> print_art("VICTORY")
    """
    print(get_art(name))


